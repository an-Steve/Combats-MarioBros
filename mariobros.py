"""**‚Üí Le Combat de l'univers de Mario**"""

import pandas as pd
from tabulate import tabulate
import random

# Charger le dataset
df = pd.read_csv("mario_dataset.csv")

# V√©rifier les colonnes
print("Colonnes disponibles :")
print(df.columns.tolist())

col_personnages = 'player_id'  # Colonne des personnages

# S√©lectionner 10 personnages al√©atoires
df_10 = df.sample(n=10, random_state=42).reset_index(drop=True)

# Afficher les 10 personnages jouables
def afficher_personnages():
    tableau = [[idx + 1, pers] for idx, pers in enumerate(df_10[col_personnages])]
    print(tabulate(tableau, headers=["N¬∞", "Personnage"], tablefmt="fancy_grid"))

# Choix d'un personnage
def choisir_personnage():
    afficher_personnages()
    while True:
        try:
            choix = int(input("Choisis le num√©ro d'un personnage (1-10) : "))
            if 1 <= choix <= 10:
                personnage = df_10.iloc[choix-1]
                return personnage
            else:
                print("‚ö†Ô∏è Veuillez choisir un num√©ro entre 1 et 10.")
        except ValueError:
            print("‚ö†Ô∏è Entr√©e invalide. Veuillez entrer un num√©ro.")

# Combat al√©atoire entre deux personnages
def combat(p1, p2):
    gagnant = random.choice([p1[col_personnages], p2[col_personnages]])
    print(f"\nCombat entre {p1[col_personnages]} et {p2[col_personnages]} :")
    print(f"üèÜ Le gagnant est {gagnant} !")

# Jeu principal
print("Bienvenue dans le jeu de combat Mario !\n")
personnage1 = choisir_personnage()
personnage2 = choisir_personnage()
combat(personnage1, personnage2)